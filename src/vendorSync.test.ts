import { describe, it, expect } from 'vitest';
import { readFileSync, existsSync } from 'fs';
import path from 'path';

/**
 * The shared A-Frame preview scripts have a SINGLE source of truth in the
 * `a-frame-shaderloader/` submodule's `js/` dir. The vite `fs-vendor-sync`
 * plugin copies them into the app (`public/js`) and the ShaderCarousel bench
 * (`ShaderCarousel/components/three`) at dev/build start.
 *
 * This suite fails on DRIFT — a consumer copy that differs from the source
 * (e.g. someone hand-edits `public/js/a-frame-shaderloader-0.4.js` instead of
 * the submodule source). Fix drift by editing the submodule source and
 * re-running `vite` (dev or build), never by editing a copy.
 *
 * The A-Frame IIFE bundle is an untracked build artifact (emitted by
 * `build/build.mjs`, regenerated by the sync plugin), so on a fresh clone /
 * CI run that hasn't built yet the SOURCE may be absent. In that case the
 * comparison is skipped (nothing to verify) rather than failed — but if the
 * source EXISTS and a copy is missing or differs, that's real drift and fails.
 */
const ROOT = path.resolve(__dirname, '..');
const SRC = path.join(ROOT, 'a-frame-shaderloader/js');

const COPIES: Record<string, string[]> = {
  'a-frame-180-a-01.min.js': ['public/js', 'ShaderCarousel/components/three'],
  'a-frame-shaderloader-0.4.js': ['public/js'],
  'a-frame-shaderloader-0.5.js': ['public/js'],
  'aframe-orbit-controls.min.js': ['public/js'],
};

describe('vendored A-Frame scripts stay in sync with the submodule source', () => {
  for (const [file, dests] of Object.entries(COPIES)) {
    const srcPath = path.join(SRC, file);
    const srcMissing = !existsSync(srcPath);
    for (const dest of dests) {
      // Skip only when the canonical source artifact isn't present in this
      // environment (e.g. unbuilt fresh clone). A missing copy WITH a present
      // source is genuine drift and must fail below.
      it.skipIf(srcMissing)(
        `${dest}/${file} is byte-identical to a-frame-shaderloader/js/${file}`,
        () => {
          const dstPath = path.join(ROOT, dest, file);
          expect(existsSync(dstPath), `copy missing — sync did not run: ${dstPath}`).toBe(true);
          expect(
            readFileSync(dstPath).equals(readFileSync(srcPath)),
            `drift: ${dest}/${file} differs from the submodule source — edit the source, not the copy`,
          ).toBe(true);
        },
      );
    }
  }
});
